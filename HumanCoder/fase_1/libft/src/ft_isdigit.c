#include "libft.h"

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

/*
Checa se o caractere é um número (0-9) ou não.
Retorna um inteiro não-negativo (x > 0) caso verdade, 0 caso falso.
*/
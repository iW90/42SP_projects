#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}

/*
Itera a lista `lst` e aplica a função `f` para cada node.
Retorna nada.
*/